	nro_bol char(6) NOT NULL primary key ,
	nro_via char(6) NOT NULL REFERENCES VIAJES,
	nom_pas varchar (50) NULL ,
	nro_asi int NULL,
        tipo_pas char(1) null, 
        pago   decimal(6,2) null
) 
