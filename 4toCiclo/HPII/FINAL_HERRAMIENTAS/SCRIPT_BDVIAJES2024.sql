	cod_chof char(5) NOT NULL Primary Key ,
	nom_chof varchar (50) NOT NULL ,
	fec_ing datetime NULL ,
	cod_cat char(3) NOT NULL REFERENCES Categorias,
	sdo_bas decimal(7,2) NULL 
) 
